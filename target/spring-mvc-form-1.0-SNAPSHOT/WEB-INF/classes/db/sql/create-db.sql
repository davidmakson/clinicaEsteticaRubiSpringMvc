CREATE TABLE servico (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
  name VARCHAR(30),
  valor INTEGER,
  descricao VARCHAR(500),
  obs VARCHAR(500)
);

CREATE TABLE funcionario (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
  name VARCHAR(30),
  email  VARCHAR(50),
  address VARCHAR(255),
  password VARCHAR(20),
  sex VARCHAR(1),
  NUMBER INTEGER,
  COUNTRY VARCHAR(10),
  SKILL VARCHAR(500)
);

CREATE TABLE users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
  name VARCHAR(30),
  email  VARCHAR(50),
  address VARCHAR(255),
  password VARCHAR(20),
  newsletter BOOLEAN,
  framework VARCHAR(500),
  sex VARCHAR(1),
  NUMBER INTEGER,
  COUNTRY VARCHAR(10),
  SKILL VARCHAR(500)
);

CREATE TABLE agenda (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
    CONSTRAINT fk_servico
    FOREIGN KEY (id)
    REFERENCES servico(id),
  dt_agenda date , 
  funcionario VARCHAR(50),
  servico VARCHAR(50),
  cliente VARCHAR(50),
  	CONSTRAINT fk_users
    FOREIGN KEY (id)
    REFERENCES users(id),
  obs VARCHAR(500)
);